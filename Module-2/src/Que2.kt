//Kotlin Program to Print an Integer (Entered by the User),Kotlin Program to Add Two Integers
fun main()
{
    println("Enter a 1st number ")
    var num1:Int = readln().toInt()

    println("Enter a 2nd number ")
    var num2:Int = readln().toInt()

    println("Addition of $num1 and $num2 : ${num1+num2}")

}
